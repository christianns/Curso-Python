# Estos operadores sirven para comparar 2 valores y su resultado sera booleano
# Este operadores igual que (==)
#false
print (3 == 4)
#true
print (3 == 3)

# Este operador es distinto que (!=)
print (1 != 0)

# Este operador es mayor que (>)
print (3 > 10)

# Este operador es menor que (<)
print (3 < 10)

# Este operador es mayor o igual que (>=)
print (3 >= 3)

# Este operador es menor o igual que (<=)
print (3 <= 1)
