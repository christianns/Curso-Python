# Los operadores aritmeticos sirven para realizar operaciones matematicas
a = 10
b = 7

# Esto es un suma
suma = a + b
print ("La suma es:", suma)

# esto es una resta
resta = a - b
print ("La resta es:", resta)

# Esto es una multiplicacion
multiplicacion = a * b
print ("La multiplicación es:", multiplicacion)

# Esto es una division
division = a / b
print ("La división es:", division)

# Esto es una division entera
division_int = a // b
print ("La división_int es:", division_int)

#Esto es el Modúlo (resto)
division_res = a % b
print ("La división_res es:", division_res)

# Esto es un exponente
exponente = a ** b
print ("El exponente es:", exponente)
